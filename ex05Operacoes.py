n1 = float(input("Digite o primeiro número: "))
n2 = float(input("Digite o segundo número: "))

sum = n1 + n2
sub = n1 - n2
mult = n1 * n2
div = n1 / n2

# imprimindo resultados arredondados das operações
print(f"{n1} + {n2} = {sum:.0f} \n{n1} - {n2} = {sub:.0f} \n{n1} * {n2} = {mult:.0f} \n{n1} / {n2} = {div:.0f}")