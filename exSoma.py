# conversão 
num1 = int(input("Digite o primeiro número: ")) # int() converte p/ inteiro
num2 = int(input("Digite o segundo número: ")) 

soma = num1 + num2
print(soma) # operações aritméticas precisam da tipagem ou conversão

print(f"O resultado de  {num1}  +  {num2}  =  {soma}") # fString e chaves são usadas para concatenar números e strings em python